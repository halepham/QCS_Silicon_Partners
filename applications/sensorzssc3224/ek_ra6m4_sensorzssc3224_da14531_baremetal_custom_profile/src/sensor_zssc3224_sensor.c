void g_zssc3224_sensor0_read(float *sensor_data, float *temperature_data)
{
	uint8_t response[7] = {0};
	SensorZSSC3224_MeasurementData();
	SensorZSSC3224_Get_RawData(response, sizeof(response));

	//uint8_t status = response[0];
	*sensor_data = (response[1] << 16) | (response[2] << 8) | response[3];
	*temperature_data = (response[4] << 16) | (response[5] << 8) | response[6];
}
